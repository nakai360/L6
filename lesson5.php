<?php
// 以下をfor文を使用して表示してください。
// ヒント: forの中でforを３回使用

// ********1
// *******121
// ******12321
// *****1234321
// ****123454321
// ***12345654321
// **1234567654321
// *123456787654321
// 12345678987654321
for ($i = 1; $i <= 9; $i++) {//iが9以上
    for ($s = 9; $s >= $i; $s--) {//iがｊ以上の場合*
      echo "*";
    }
    for ($k = 1; $k <= $i; $k++) {//kがi以上で
      echo $k;
    }
    for ($a = $i - 1; $a >= 1; $a--) {//aがi未満で
      echo $a;
    }
    echo "<br />";//改行
  }//4行目
  
  
  ?>
  